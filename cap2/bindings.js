//Se puede cambiar su valor, no es una caja es un pulpo que sujeta y suelta
let humor = "ligero";
console.log(humor);
// → ligero
humor = "oscuro";
console.log(humor);
// → oscuro

let deudaLuigi = 140;
deudaLuigi = deudaLuigi - 35;
console.log(deudaLuigi);
// → 105

let uno = 1, dos = 2;
console.log(uno + dos);
// → 3


//Diferentes formas de declarar 
var nombre = "Ayda";
const saludo = "Hola ";
console.log(saludo + nombre);
// → Hola Ayda

